	  		if(options[0] == "icon"){
	  			$classess = "jltma-icommon";
	  		}else{
	  			$classess = "jltma-other";
	  		}
	  		$list.append('<li class="'+$classess+'" data-icon="'+icons[i]+'"><a href="#" title="'+icons[i]+'"><span class="'+options[0]+' '+options[1]+'-'+icons[i]+'"></span></a></li>');	
