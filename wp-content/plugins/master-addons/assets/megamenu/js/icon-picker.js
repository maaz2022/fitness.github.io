	function removePopup(){
		$(".icon-picker-container").remove();
	}
	

	$button = $('.icon-picker');
	$button.each( function() {
		$(this).on('click.iconPicker', function() {
			createPopup($(this));
		});
	});


	function createPopup($button) {
		$target = $($button.data('target'));
		$popup = $('<div class="icon-picker-container"> \
			<span class="icon-picker-close"><i class="dashicons dashicons-no"></i></span> \
			<div class="icon-picker-control" /> \
			<ul class="icon-picker-list" /> \
			</div>')
		.css({
			'top': $button.offset().top,
			'left': $button.offset().left
		});
		build_list($popup,$button,0);
		var $control = $popup.find('.icon-picker-control');
		$control.html('<p>Select Icon: <select><option value="dashicons">Dashicons</option><option value="linecon">Linecons</option><option value="fa">Font Awesome 4.7.0</option><optgroup label="Font Awesome 5.x"><option value="fab">Brands</option><option value="far">Regular</option><option value="fas">Solid</option></optgroup><option value="genericon">Genericons</option><option value="icon">Icomoon</option><option value="lnr">Linearicons</option><option value="ti">Themify</option></select></p>'+
			'<a data-direction="back" href="#"><span class="dashicons dashicons-arrow-left-alt2"></span></a> '+
			'<input type="text" class="jltma-icon-search" placeholder="Search" value=""/>'+
			'<a data-direction="forward" href="#"><span class="dashicons dashicons-arrow-right-alt2"></span></a>'+
			'');

		$('select', $control).on('change', function(e) {
			e.preventDefault();
			if (this.value != options[0]) {
				options[0] = this.value;
				font_set();
				build_list($popup,$button,1);
			};
		});

		$('a', $control).click(function(e) {
			e.preventDefault();
			if ($(this).data('direction') === 'back') {
				//move last 25 elements to front
				$('li:gt(' + (icons.length - 26) + ')', $list).each(function() {
					$(this).prependTo($list);
				});
			} else {
				//move first 25 elements to the end
				$('li:lt(25)', $list).each(function() {
					$(this).appendTo($list);
				});
			}
		});

		$popup.appendTo('body').show();

		$('.jltma-icon-search', $control).on('keyup', function(e) {
			console.log('clicked');
			var search = $(this).val();
			console.log('clicked', search);

			if (search === '') {
				//Show All Again
				$('li:lt(25)', $list).show();
			} else {
				$('li', $list).each(function() {
					if ($(this).data('icon').toString().toLowerCase().indexOf(search.toLowerCase()) !== -1) {
						$(this).show();
					} else {
						$(this).hide();
					}
				});
			}
		});

		$(document).mouseup(function (e){
			if (!$popup.is(e.target) && $popup.has(e.target).length === 0) {
				removePopup();
			}
		});

		$(document).on("click",".icon-picker-close",function(e){ 
			removePopup();
		});

		$(document).on('keydown',function(e){
			if (e.keyCode === 27) {
				removePopup();
			}
		});
	}
